import { getArraySum } from "./common.js"

export const part1 = (): number =>
    getArraySum(
        parseInputsRound1().map(getRoundScore)
    );

export const part2 = () => {
    return getArraySum(
        parseInputsRound2()
        .map(round => {
            const plays: IRound = {
                opponent: round.opponent,
                player: getPlayerChoice(round)
            };
            return plays;
        })
        .map(getRoundScore)
    );
};

const parseInputsRound1 = (): Array<IRound> => {
    return input.split("\n").map(round => {
        const plays = round.split(" ");
        return {
            player: <PlayerChoice><keyof typeof PlayerChoice>plays[1],
            opponent: <OpponentChoice><keyof typeof PlayerChoice>plays[0]
        }
    });
};

export const getRoundScore = (round: IRound): number => {
    const { player, opponent } = round;
    if (player === PlayerChoice.Rock) {
        if (opponent === OpponentChoice.Scissors) {
            return 7;
        }
        if (opponent === OpponentChoice.Rock) {
            return 4;
        }
        // Paper
        return 1;
    }
    if (player === PlayerChoice.Paper) {
        if (opponent === OpponentChoice.Rock) {
            return 8;
        }
        if (opponent === OpponentChoice.Scissors) {
            return 2;
        }
        // Paper
        return 5;
    }
    // Scissors
    if (opponent === OpponentChoice.Paper) {
        return 9;
    }
    if (opponent === OpponentChoice.Rock) {
        return 3;
    }
    // Scissors
    return 6;
};

const parseInputsRound2 = (): Array<IRoundResultPart2> => {
    return input.split("\n").map(round => {
        const plays = round.split(" ");
        return {
            result: <RoundResult><keyof typeof RoundResult>plays[1],
            opponent: <OpponentChoice><keyof typeof PlayerChoice>plays[0]
        }
    });
};

const getPlayerChoice = (round: IRoundResultPart2): PlayerChoice => {
    if (round.result === RoundResult.Win) {
        if (round.opponent === OpponentChoice.Paper) {
            return PlayerChoice.Scissors;
        }
        if (round.opponent === OpponentChoice.Rock) {
            return PlayerChoice.Paper;
        }
        // Scissors
        return PlayerChoice.Rock;
    }
    if (round.result === RoundResult.Draw) {
        if (round.opponent === OpponentChoice.Paper) {
            return PlayerChoice.Paper;
        }
        if (round.opponent === OpponentChoice.Rock) {
            return PlayerChoice.Rock;
        }
        // Scissors
        return PlayerChoice.Scissors;
    }

    // Loose
    if (round.opponent === OpponentChoice.Paper) {
        return PlayerChoice.Rock;
    }
    if (round.opponent === OpponentChoice.Rock) {
        return PlayerChoice.Scissors;
    }
    // Scissors
    return PlayerChoice.Paper;
};

export interface IRound {
    player: PlayerChoice;
    opponent: OpponentChoice;
}

